    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed

         try{
            String sql = "select * from payment_table where pat_id = ?";
            pst = con.prepareStatement(sql);
            pst.setInt(1,Integer.parseInt(patient_id_txt.getText()));
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                patient_name_txt.setText(rs.getString("name"));
                service_textarea.setText(rs.getString("service_desc"));
                total_amount_txt.setText(rs.getString("total_amount"));
                 submitted_txt.setText(rs.getString("submitted_amount"));
                 payment_status_combo.setSelectedItem(rs.getString("payment_status"));
                 payment_method_combo.setSelectedItem(rs.getString("payment_method"));
                
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Record not found!!");
            }
               con.close();
               
        }
       
       catch(Exception e){
         JOptionPane.showMessageDialog(null, e);
     }
    }//GEN-LAST:event_search_btnActionPerformed

