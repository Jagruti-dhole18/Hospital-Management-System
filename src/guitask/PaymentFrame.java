    private void pay_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pay_btnActionPerformed
         if(name_txt.getText().equals(""))
        JOptionPane.showMessageDialog(null,"name is mandetory");
        else if(card_number_txt.getText().equals(""))
        JOptionPane.showMessageDialog(null,"card number is mandetory");
        else if(security_code_txt.getText().equals(""))
        JOptionPane.showMessageDialog(null,"card number is mandetory");
        else if(day_txt.getText().equals("") && month_txt.getText().equals("") )
        JOptionPane.showMessageDialog(null," expiry date is mandetory");
        else if(phone_txt.getText().equals("") )
        JOptionPane.showMessageDialog(null," phone number is mandetory");
         else if(email_txt.getText().equals("") )
        JOptionPane.showMessageDialog(null," Email ID is mandetory");
        else
        JOptionPane.showMessageDialog(null,"Payment successful!!");
        
        
        
         try{
           String sql = "insert into payment_process_table values (?,?,?,?,?,?,?)" ;
           pst = con1.prepareStatement(sql);
           pst.setString(1,patient_name_txt.getText());
           pst.setString(2,card_number_txt.getText());
            pst.setInt(3, Integer.parseInt(security_code_txt.getText()));
            pst.setInt(4, Integer.parseInt(day_txt.getText()));
            pst.setInt(5, Integer.parseInt(month_txt.getText()));
           pst.setString(6,phone_txt.getText());
           pst.setString(7,email_txt.getText());
           
           pst.executeUpdate();
           con1.close();
          
       }
        catch(NumberFormatException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_pay_btnActionPerformed

