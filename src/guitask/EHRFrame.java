    private void ehr_search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ehr_search_btnActionPerformed
        try{
            String sql = "select * from electronic_health_record_table where patient_id = ?";
            pst = con.prepareStatement(sql);
            pst.setInt(1,Integer.parseInt(ehr_patient_id_txt.getText()));
            rs = pst.executeQuery();
            
    if (rs.next()) {
        String patientName = rs.getString("patient_name");
        ehr_patient_name_txt.setText(patientName != null ? patientName : "");

        String pastIllness = rs.getString("past_illness");
        past_illness_txt.setText(pastIllness != null ? pastIllness : "");

        String chronicDisease = rs.getString("chronic_disease");
        chronic_txt.setText(chronicDisease != null ? chronicDisease : "");

        String allergies = rs.getString("allergies");
        allergies_txt.setText(allergies != null ? allergies : "");

        String surgeriesUndergone = rs.getString("surgeries_undergone");
        surgeries_txt.setText(surgeriesUndergone != null ? surgeriesUndergone : "");

        String vitalSign = rs.getString("vital_sign");
        vital_sign_txt.setText(vitalSign != null ? vitalSign : "");

        String medication = rs.getString("medication");
        medication_txt.setText(medication != null ? medication : "");

        String testDone = rs.getString("test_done");
        test_txt.setText(testDone != null ? testDone : "");

        String physician = rs.getString("physician");
        physician_name.setText(physician != null ? physician : "");
    } 
    else
        {
        JOptionPane.showMessageDialog(null, "Record not found!!");
         }

    con.close();

} catch (HeadlessException | NumberFormatException | SQLException e) {
    JOptionPane.showMessageDialog(null, e);
}


    }//GEN-LAST:event_ehr_search_btnActionPerformed

