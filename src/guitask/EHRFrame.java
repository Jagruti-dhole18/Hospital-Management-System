    private void save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_save_btnActionPerformed

       if(ehr_patient_id_txt.getText().equals(""))
           JOptionPane.showMessageDialog(null,"ID is mandetory");
       else if(ehr_patient_name_txt.getText().equals(""))
           JOptionPane.showMessageDialog(null,"Name is mandetory");
       else if(medication_txt.getText().equals(""))
           JOptionPane.showMessageDialog(null,"medication field is mandetory");
       else if(test_txt.getText().equals(""))
           JOptionPane.showMessageDialog(null,"This field is mandetory");
       else if(physician_name.getText().equals(""))
           JOptionPane.showMessageDialog(null,"physician name is mandetory");
       else 
           JOptionPane.showMessageDialog(null,"Record save successfully!");
       
       
       try{
           String sql = "insert into electronic_health_record_table values(?,?,?,?,?,?,?,?,?,?)";
           pst = con.prepareStatement(sql);
           pst.setInt(1, Integer.parseInt(ehr_patient_id_txt.getText()));
           pst.setString(2, ehr_patient_name_txt.getText());
           pst.setString(3, past_illness_txt.getText());
           pst.setString(4, chronic_txt.getText());
           pst.setString(5, allergies_txt.getText());
           pst.setString(6, surgeries_txt.getText());
           pst.setString(7, vital_sign_txt.getText());
           pst.setString(8, medication_txt.getText());
           pst.setString(9, test_txt.getText());
           pst.setString(10, physician_name.getText());
           
           pst.executeUpdate();
           con.close();
       }
       catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
       }
       
    }//GEN-LAST:event_save_btnActionPerformed

